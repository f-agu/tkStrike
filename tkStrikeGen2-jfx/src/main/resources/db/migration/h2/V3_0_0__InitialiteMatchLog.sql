CREATE TABLE TKS_MATCH_LOG
(
	ID VARCHAR(45) NOT NULL	PRIMARY KEY,
	VERSION INTEGER,
	MATCH_CONFIGURATION_ID VARCHAR(45),
	NUMBER_OF_JUDGES INTEGER,
	MIN_BODY_LEVEL INTEGER,
	BODY_SENSORS_ENABLED BOOLEAN,
	MIN_HEAD_LEVEL INTEGER,
	HEAD_SENSORS_ENABLED BOOLEAN,
	MATCH_START_TIME DECIMAL(16),
	MATCH_END_TIME DECIMAL(16),
	BODY_POINTS INTEGER,
	HEAD_POINTS INTEGER,
	PUNCH_POINTS INTEGER,
	BODY_TECH_POINTS INTEGER,
	HEAD_TECH_POINTS INTEGER,
	OVERTIME_POINTS INTEGER,
	CELLING_SCORE INTEGER,
	DIFFERENCIAL_SCORE INTEGER,
	NEAR_MISS_LEVEL INTEGER,
	MATCH_NUMBER VARCHAR(15),
	PHASE_ID VARCHAR(45),
	CATEGORY_ID VARCHAR(45),
	BLUE_ATHLETE_ID VARCHAR(45),
	BLUE_VIDEO_QUOTA INTEGER,
	RED_ATHLETE_ID VARCHAR(45),
	RED_VIDEO_QUOTA INTEGER,
	ROUNDS INTEGER,
	ROUND_TIME VARCHAR(5),
	KYESHI_TIME VARCHAR(5),
	REST_TIME VARCHAR(5),
	GOLDENPOINT_ENABLED BOOLEAN,
	GOLDENPOINT_TIME VARCHAR(5),
	MATCH_WINNER VARCHAR(8),
	MATCH_WINNER_BY VARCHAR(3),
	MATCH_RESULT VARCHAR(25),
	IS_PARATKD_MATCH BOOLEAN DEFAULT FALSE,
	MAX_GAM_JEOMS INTEGER,
	GDP_TIE_BREAKER BOOLEAN DEFAULT FALSE,
	GDP_BLUE_ROUND_WINS INTEGER DEFAULT NULL,
	GDP_BLUE_HITS INTEGER DEFAULT NULL,
	GDP_BLUE_PENALTIES INTEGER DEFAULT NULL,
	GDP_RED_ROUND_WINS INTEGER DEFAULT NULL,
	GDP_RED_HITS INTEGER DEFAULT NULL,
	GDP_RED_PENALTIES INTEGER DEFAULT NULL,
	ROUNDS_WINNER VARCHAR(255) DEFAULT 'NULL',
	OVERTIME_PENALTIES INTEGER DEFAULT 2 NOT NULL,
	MATCH_VICTORY_CRITERIA VARCHAR(10) DEFAULT 'BY_POINTS' NOT NULL,
	GDP_BLUE_PUNCHES INTEGER DEFAULT NULL,
	GDP_RED_PUNCHES INTEGER DEFAULT NULL,
	BLUE_PARA_TECH_POINTS INTEGER DEFAULT NULL,
	RED_PARA_TECH_POINTS INTEGER DEFAULT NULL
);

